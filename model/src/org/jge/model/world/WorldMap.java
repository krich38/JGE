    public abstract Tile tileAt(int x, int y);

    private static class MapImpl extends WorldMap {
        private int[][] map;
        private Tile[][] tiles;
        public MapImpl(int[][] map) {
            super();
            this.map = map;
            this.tiles = new Tile[World.TILES_X][World.TILES_Y];
        }

        public Tile tileAt(int x, int y) {
            Tile t = tiles[x][y];
            if(t == null) {
                t = new Tile(map[x][y], new Waypoint(x,y));
                tiles[x][y] = t;
            }
            return t;

        }
    }
